    let currentLang = 'zh-CN';

    function t(key, vars = {}) {
      const langData = TRANSLATIONS[currentLang] || {};
      const fallback = TRANSLATIONS['zh-CN'];
      let str = (key in langData) ? langData[key] : fallback[key];
      if (str === undefined) return key;
      return str.replace(/\{(\w+)\}/g, (_, k) => (k in vars) ? vars[k] : `{${k}}`);
    }

    function applyTranslations() {
      document.documentElement.lang = currentLang;
      document.querySelectorAll('[data-i18n]').forEach(el => {
        el.textContent = t(el.dataset.i18n);
      });
      // CSS variables for ::after pseudo-element content
      document.documentElement.style.setProperty('--label-expand', `"${t('expand')}"`);
      document.documentElement.style.setProperty('--label-collapse', `"${t('collapse')}"`);
    }

